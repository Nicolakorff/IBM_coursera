def area_of_rectangle(a, b) :
    """
    This function returns the area of a rectangle with the given length and breadth.
    The area is calculated as the multiplication of the lenght and breadth.
    """
    return a * b

def area_of_circle(r) :
    """ 
    This function returns tje area of a circle with the given radius.
    The area is calculated as the multiplication of pi and the radius squared.
    """
    return 3.14 * (r**2)